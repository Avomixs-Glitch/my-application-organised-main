/**
 * Obtient le logo personnalisé pour un domaine donné
 */
export function getCustomLogo(url: string): string | undefined {
  try {
    const domain = new URL(url).hostname.replace("www.", "");
    return customLogos[domain];
  } catch (error) {
    return undefined;
  }
}

/**
 * Vérifie si un logo personnalisé existe pour ce domaine
 */
export function hasCustomLogo(url: string): boolean {
  return getCustomLogo(url) !== undefined;
}
